import math

R = 1737100
r = 4 * R
a = 2.5 * R
r1 = R
r2 = 2 * a - r1
e = 1 - r1 / a
G = 6.67 * pow(10, -11)
M = 7.35 * pow(10, 22)
v = (G * M / r) ** 0.5
v1 = (G * M / a * (1 - e) / (1 + e)) ** 0.5
v2 = (G * M / a * (1 + e) / (1 - e)) ** 0.5
dv = v - v1
t = 0.5 * (4 * math.pi ** 2 * r ** 3 / G / M) ** 0.5

print("Характеристическая скорость:", abs(round(dv)) / 1000, "км/с")
print("Скорость v2:", round(v2) / 1000, "км/с")
print("Время перелета:", round(t / 3600, 1), "ч")
